import { writeFile } from 'node:fs/promises';

const CHRONODEX_HOME = 'https://chronostorydex.com/';
const CHRONODEX_SOURCE = 'https://chronostorydex.com/';
const OUTPUT = new URL('../src/data/chronoScroll.ts', import.meta.url);

function extractJsonArrays(js) {
  const arrays = [];
  const regex = /([A-Za-z_$][\w$]*)=JSON\.parse\(([`'])/g;
  let match;
  while ((match = regex.exec(js))) {
    const quote = match[2];
    const start = regex.lastIndex;
    const end = js.indexOf(`${quote})`, start);
    if (end === -1) continue;
    const raw = js.slice(start, end);
    try {
      const data = JSON.parse(raw);
      if (Array.isArray(data)) arrays.push({ name: match[1], data });
    } catch {
      // Not a simple JSON literal; ignore.
    }
  }
  return arrays;
}

function asNumber(value) {
  return typeof value === 'number' && Number.isFinite(value) ? value : undefined;
}

function pickStatEffects(metaInfo = {}) {
  const effects = {};
  for (const [key, value] of Object.entries(metaInfo)) {
    if (!key.startsWith('inc')) continue;
    const amount = asNumber(value);
    if (!amount) continue;
    effects[key.replace(/^inc/, '')] = amount;
  }
  return effects;
}

function toIconUrl(itemId) {
  return `https://maplestory.io/api/GMS/83/item/${itemId}/icon`;
}

function normalizeEquipment(row) {
  const itemId = asNumber(row.itemID);
  const meta = row.metaInfo ?? {};
  const type = row.typeInfo ?? {};
  return {
    id: String(itemId),
    mapleItemId: itemId,
    name: row.ServerItemName || row.description?.name || `Item ${itemId}`,
    category: row.equipGroup || type.category || type.subCategory || 'Equip',
    slots: asNumber(meta.tuc),
    reqLevel: asNumber(meta.reqLevelEquip) ?? asNumber(meta.reqLevel),
    iconUrl: itemId ? toIconUrl(itemId) : undefined,
    chronodexUrl: `${CHRONODEX_HOME}?item=${itemId}`,
  };
}

function normalizeScroll(row) {
  const itemId = asNumber(row.itemID);
  const imageId = asNumber(row.overrideImage) ?? itemId;
  const effects = pickStatEffects(row.metaInfo ?? {});
  return {
    id: String(itemId),
    mapleItemId: itemId,
    name: row.ServerItemName || row.description?.name || `Scroll ${itemId}`,
    category: row.category || row.typeInfo?.subCategory || 'Scroll',
    scrollType: row.scrollType,
    successChance: asNumber(row.scrollPercent),
    effects,
    description: row.description?.description,
    iconUrl: imageId ? toIconUrl(imageId) : undefined,
    chronodexUrl: `${CHRONODEX_HOME}?item=${itemId}`,
  };
}

function byIdThenName(a, b) {
  return Number(a.id) - Number(b.id) || a.name.localeCompare(b.name);
}

const fetchedAt = new Date().toISOString();
const home = await fetch(CHRONODEX_HOME);
if (!home.ok) throw new Error(`ChronoDEX home fetch failed: ${home.status}`);
const html = await home.text();
const asset = html.match(/src="([^"]*assets\/index-[^"]+\.js)"/)?.[1];
if (!asset) throw new Error('Could not locate ChronoDEX app asset.');
const assetUrl = new URL(asset, CHRONODEX_HOME).href;
const response = await fetch(assetUrl);
if (!response.ok) throw new Error(`ChronoDEX asset fetch failed: ${response.status}`);
const js = await response.text();
const arrays = extractJsonArrays(js);

const scrollRows = arrays.flatMap(({ data }) =>
  data.filter((row) => row && typeof row === 'object' && typeof row.itemID === 'number' && row.scrollPercent !== undefined),
);
const equipmentRows = arrays.flatMap(({ data }) =>
  data.filter((row) => row && typeof row === 'object' && typeof row.itemID === 'number' && row.equipGroup && row.typeInfo?.overallCategory === 'Equip'),
);

if (scrollRows.length < 50) throw new Error(`Unexpectedly few scroll rows: ${scrollRows.length}`);
if (equipmentRows.length < 200) throw new Error(`Unexpectedly few equipment rows: ${equipmentRows.length}`);

const seenEquip = new Set();
const equipment = equipmentRows
  .filter((row) => {
    const id = String(row.itemID);
    if (seenEquip.has(id)) return false;
    seenEquip.add(id);
    return true;
  })
  .map(normalizeEquipment)
  .sort(byIdThenName);

const seenScroll = new Set();
const scrolls = scrollRows
  .filter((row) => {
    const id = String(row.itemID);
    if (seenScroll.has(id)) return false;
    seenScroll.add(id);
    return true;
  })
  .map(normalizeScroll)
  .sort(byIdThenName);

const output = `// Generated by scripts/generate-scroll-data.mjs. Do not edit by hand.\nimport type { ScrollSimDataset } from '../lib/scroll/types';\n\nexport const chronoScrollDataset = ${JSON.stringify({
  equipment,
  scrolls,
  metadata: {
    label: 'ChronoDEX public equipment and scroll data',
    sourceUrl: CHRONODEX_SOURCE,
    assetUrl,
    fetchedAt,
    notes: [
      'Equipment slot counts are ChronoDEX metaInfo.tuc values when present.',
      'Scroll success chances and stat deltas are ChronoDEX scrollPercent/metaInfo inc* values.',
      'Destruction/boom behavior and special scroll mechanics are not simulated unless represented in this checked-in public dataset.',
    ],
  },
}, null, 2)} as ScrollSimDataset;\n`;
await writeFile(OUTPUT, output);
console.log(`Wrote ${equipment.length} equipment and ${scrolls.length} scrolls from ${assetUrl}`);
